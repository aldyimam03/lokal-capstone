package aiw.mobile.view.dashboard.ui.dashboard

import aiw.mobile.testonboardingpage.databinding.FragmentDashboardBinding
import aiw.mobile.view.type_of_diseases.TypeOfDiseasesActivity
import android.content.Intent
import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment

class DashboardFragment : Fragment() {

    private var _binding: FragmentDashboardBinding? = null

    // This property is only valid between onCreateView and
    // onDestroyView.
    private val binding get() = _binding!!

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {

        _binding = FragmentDashboardBinding.inflate(inflater, container, false)
        val root: View = binding.root

        val banner = binding.imageView15
        banner.setOnClickListener {
            val intent = Intent(requireContext(), TypeOfDiseasesActivity::class.java)
            startActivity(intent)
        }
        return root
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}